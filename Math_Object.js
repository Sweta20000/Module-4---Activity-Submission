console.log(Math.round(4.7));   // 5 (round to nearest integer)
console.log(Math.floor(4.7));   // 4 (round down)
console.log(Math.ceil(4.2));    // 5 (round up)
console.log(Math.max(10, 25));  // 25 (largest value)
console.log(Math.min(10, 25));  // 10 (smallest value)
console.log(Math.pow(2, 3));    // 8 (2³)
console.log(Math.abs(-15));     // 15 (absolute value)
console.log(Math.random());     // random number between 0 and 1