let skills = ["HTML", "CSS"];
skills.push("Bootstrap"); 
console.log(skills); // ["HTML", "CSS", "Bootstrap"]
skills.pop(); 
console.log(skills); // ["HTML", "CSS"]
skills.unshift("JavaScript"); 
console.log(skills); // ["JavaScript", "HTML", "CSS"]
skills.shift(); 
console.log(skills); // ["HTML", "CSS"]